"""
+ Suma
- Resta
* Multiplicacion
/ Divicion
% Modulo
** Exponte
// Divicion exacta

Ejemplos:
"""

A = 10
B = 5.5

# Suma
print("Suma:", A+B, "\n")

# Resta
print("Resta", A-B, "\n")

# Multiplicacion
print("Multiplicacion", A*B, "\n")

# Divicion
print("Divicion", A/B, "\n")

# Modulo
print("Modulo", A%B, "\n")

# Exponte
print("Exponente", A**B, "\n")

# Divicion exacta

print ("Divicion exacta", A//B, "\n")